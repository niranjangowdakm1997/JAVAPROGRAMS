package ArrayPrograms;

public class PyramidProgram {
	
	public static void main(String[] args) {
		
		int no=5;
		
		for (int i=1; i<=no; i++) {
			
		
			for (int j=2; j<=0; j--) {
				
				System.out.print("@");
			
			}
			
			for (int j=1; j<=5; j++) {
				
				System.out.print(j+"");
				
			}
		}
	}

}
